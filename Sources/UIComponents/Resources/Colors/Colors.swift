import SwiftUI

public extension Color {
    struct CustomColor {
        public static var green: Color  { return Color(#colorLiteral(red: 0, green: 0.5659442544, blue: 0.287532568, alpha: 1)) }
        public static var darkBlue: Color  { return Color(#colorLiteral(red: 0.1018853113, green: 0.1138664857, blue: 0.3018276095, alpha: 1)) }
        public static var grayishBlue: Color  { return Color(#colorLiteral(red: 0.262745098, green: 0.262745098, blue: 0.3882352941, alpha: 1)) }
        public static var white: Color  { return Color(#colorLiteral(red: 1, green: 0.9999999404, blue: 0.9999999404, alpha: 1)) }
    }
}

